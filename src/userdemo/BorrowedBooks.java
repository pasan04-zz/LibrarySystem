        public void executeQuery(String query,String message){
            
            Connection connection = DBConnection.createConnection();
            Statement st;
            try{
                st = connection.createStatement();
                if((st.executeUpdate(query)) == 1){
                    DefaultTableModel model = (DefaultTableModel) jTable_display_books.getModel();
                    model.setRowCount(0);
                    showBooks_in_jtable();
                    JOptionPane.showMessageDialog(null,"Data  "+message+"succesfullly");
                    
                }
                else{
                     JOptionPane.showMessageDialog(null,"Data not "+message);
                }
                
                
            }catch(Exception ex){
                
                ex.printStackTrace();
                
            }

            
            
        }
    
      public ArrayList<borrowbooks> userbookList(){
            
            ArrayList<borrowbooks> borrowbooklist = new ArrayList<borrowbooks>();
            Connection connection = DBConnection.createConnection();
            String query = "SELECT * FROM borrowbooks";
            Statement st;
            ResultSet rs;
            
            
            try{
                st = connection.createStatement();
                rs = st.executeQuery(query);
                borrowbooks b1;
                while(rs.next()){
                    b1 = new borrowbooks(rs.getInt("id"),rs.getString("name"),rs.getString("person"));
                    borrowbooklist.add(b1);
                }
                
            }catch(Exception e){
                
                e.printStackTrace();
                
            }   
            return borrowbooklist;
       
        }
